#!/bin/bash
set -eu
ls -lrt
echo "====================================================================="
echo "====================================================================="
echo "============ $PREFIX ============="
echo "====================================================================="
pwd
#ECCODES_SRCDIR=$(pwd)
#echo $ECCODES_SRCDIR
mkdir -p build
cd build
rm -f CMakeCache.txt  # For rerunning with build-wheel.py --no-unpack.
ls -lrt 
pwd                
echo "======================================================================================================="
echo "=================================           Start cmake                        ========================"
echo "======-DIEEE_LE=1 =======================================================================================" 
#cmake -DCMAKE_VERBOSE_MAKEFILE:BOOL=ON  
cmake .. -DIEEE_LE=1 -DIEEE_BE=1 \
-DDISABLE_OS_CHECK=ON -DENABLE_AEC=OFF -DCMAKE_VERBOSE_MAKEFILE:BOOL=ON -DENABLE_FORTRAN=OFF -DCOMPILE_TESTS=NO -DBUILD_SHARED_LIBS=1 \
   -DCMAKE_INSTALL_PREFIX="$PREFIX" 
echo "======================================================================================================="
echo "======================================================================================================="
echo "=====================      cmake ends      ignore  ctest    ==========================================="
echo "======================================================================================================="
echo "======================================================================================================="
make  -j $(nproc) 
echo "=====================      Fin make Process ECCODES      ================================"
make install
